function translateAdvancedTerms() {
    $filePath = 'resources/lang/uk.json';
    
    // Read file
    $jsonContent = file_get_contents($filePath);
    $data = json_decode($jsonContent, true);
    
    if (!$data) {
        echo "❌ Error: Could not parse JSON\n";
        return false;
    }
    
    $translations = getAdvancedTranslations();
    $changesMade = 0;
    $totalTerms = count($data);
    
    echo "🚀 ADVANCED TRANSLATION - Targeting 70%+ Completion\n";
    echo "================================================\n\n";
    echo "📊 Total terms: $totalTerms\n";
    echo "🗂️ Advanced translation map size: " . count($translations) . "\n\n";
    
    // Apply advanced translations
    foreach ($data as $key => $value) {
        if (is_string($value) && isset($translations[$value])) {
            $data[$key] = $translations[$value];
            $changesMade++;
            echo "✅ $key: '$value' → '{$translations[$value]}'\n";
        }
    }
    
    echo "\n📈 Advanced translations applied: $changesMade\n";
    
    // Save file
    $jsonOutput = json_encode($data, JSON_UNESCAPED_UNICODE | JSON_PRETTY_PRINT);
    if (file_put_contents($filePath, $jsonOutput)) {
        echo "\n✅ File saved successfully!\n";
        
        // Final statistics
        $ukrainianCount = 0;
        foreach ($data as $value) {
            if (is_string($value) && preg_match('/[А-Яа-яІіЇїЄєҐґ]/', $value)) {
                $ukrainianCount++;
            }
        }
        
        $percentage = round(($ukrainianCount / $totalTerms) * 100, 1);
        echo "\n🎉 ADVANCED STATISTICS:\n";
        echo "======================\n";
        echo "📊 Total changes made: $changesMade\n";
        echo "📈 Ukrainian terms: $ukrainianCount / $totalTerms ($percentage%)\n";
        echo "🎯 Remaining English terms: " . ($totalTerms - $ukrainianCount) . "\n";
        
        if ($percentage >= 70) {
            echo "\n🎊 EXCELLENT! TARGET ACHIEVED! 🇺🇦\n";
            echo "🏆 You've reached 70%+ Ukrainian localization!\n";
            echo "🌟 Outstanding translation progress!\n";
        } else if ($percentage >= 65) {
            echo "\n🎉 GREAT PROGRESS! 🇺🇦\n";
            echo "🚀 Almost at 70% target!\n";
        } else {
            echo "\n📈 STEADY PROGRESS! 🇺🇦\n";
            echo "🎯 Moving towards 70% target!\n";
        }
        
        return true;
    } else {
        echo "❌ Error saving file\n";
        return false;
    }
}

