<script>
// Анімація лічильників
$(document).ready(function() {
    $('.counter').each(function() {
        const $this = $(this);
        const target = parseInt($this.data('target'));
        
        $({ counter: 0 }).animate({
            counter: target
        }, {
            duration: 2000,
            easing: 'easeOutExpo',
            step: function() {
                $this.text(Math.ceil(this.counter).toLocaleString());
            },
            complete: function() {
                $this.text(target.toLocaleString());
            }
        });
    });
});
</script>
