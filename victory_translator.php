    foreach ($data as $value) {
        if (is_string($value) && preg_match('/[А-Яа-яІіЇїЄєҐґ]/', $value)) {
            $currentUkrainianCount++;
        }
    }
    $needed = $target70Percent - $currentUkrainianCount;
    
