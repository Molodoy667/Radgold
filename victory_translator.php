        echo "=====================\n";
        echo "📊 Total changes made: $changesMade\n";
        echo "📈 Ukrainian terms: $ukrainianCount / $totalTerms ($percentage%)\n";
        echo "🎯 Remaining English terms: " . ($totalTerms - $ukrainianCount) . "\n";
        
